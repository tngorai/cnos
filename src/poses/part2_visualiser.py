def get_mesh(obj_id: int) -> Optional[trimesh.Trimesh]:
    """Load a .ply CAD model for a given object ID. Cached after first load."""
    if obj_id in _mesh_cache:
        return _mesh_cache[obj_id]
    ply_path = os.path.join(MODEL_DIR, f"obj_{obj_id:06d}.ply")
    if not os.path.exists(ply_path):
        print(f"Warning: mesh not found at {ply_path}")
        _mesh_cache[obj_id] = None
        return None
    mesh = trimesh.load(ply_path)
    _mesh_cache[obj_id] = mesh
    print(f"Loaded mesh for obj_{obj_id:06d}: {len(mesh.vertices)} vertices, {len(mesh.faces)} faces")
    return mesh


