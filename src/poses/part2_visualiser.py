# MATH HELPERS
# =============================================================================
def rotation_error_deg(R_gt: np.ndarray, R_pred: np.ndarray) -> float:
    """Angular distance between two 3x3 rotation matrices, in degrees.
    
    Formula: θ = arccos((trace(R_gt^T * R_pred) - 1) / 2)
    This comes from the axis-angle representation: for any rotation matrix R,
    trace(R) = 1 + 2·cos(θ). Applying this to the difference rotation R_diff
    = R_gt^T · R_pred gives the net rotation angle between them.
    """
    R_diff = R_gt.T @ R_pred
    trace = np.trace(R_diff)
    cos_theta = np.clip((trace - 1.0) / 2.0, -1.0, 1.0)
    return float(np.degrees(np.arccos(cos_theta)))


def translation_error_mm(t_gt: np.ndarray, t_pred: np.ndarray) -> float:
    """Euclidean distance between two translation vectors, in mm."""
    return float(np.linalg.norm(t_gt - t_pred))


def compute_iou(mask_a: np.ndarray, mask_b: np.ndarray) -> float:
    """Intersection-over-Union for two binary masks.
    
    IOU = |A ∩ B| / |A ∪ B|, where |·| counts non-zero pixels.
    Range: [0, 1].  1.0 = perfect overlap, 0.0 = no overlap.
    """
    intersection = np.logical_and(mask_a, mask_b).sum()
    union = np.logical_or(mask_a, mask_b).sum()
    if union == 0:
        return 1.0  # both empty → perfect agreement
    return float(intersection / union)


def matrix_to_quat_wxyz(R: np.ndarray) -> np.ndarray:
    """Convert 3x3 rotation matrix to wxyz quaternion (Viser convention)."""
    return tf.SO3.from_matrix(R).wxyz

