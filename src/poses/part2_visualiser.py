# 2D RENDERING: draw bounding boxes & masks onto the RGB image
# =============================================================================
def render_2d_composite(
    frame_id: int,
    scene_gt: dict,
    scene_gt_info: dict,
    predictions: dict,
    show_gt: bool,
    show_pred: bool,
) -> np.ndarray:
    """Return an RGBA numpy array of the RGB image with GT/Pred overlays drawn."""
    rgb_path = os.path.join(RGB_DIR, f"{frame_id:0{_FRAME_PAD}d}.png")
    if not os.path.exists(rgb_path):
        return np.zeros((480, 640, 4), dtype=np.uint8)

    img = cv2.imread(rgb_path)
    img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    # Convert to RGBA PIL image for alpha-composited drawing
    pil_img = Image.fromarray(img).convert("RGBA")
    overlay = Image.new("RGBA", pil_img.size, (0, 0, 0, 0))
    draw = ImageDraw.Draw(overlay)

    f_str = str(frame_id)

    def draw_bbox(draw_obj, bbox, colour, label: str):
        """bbox format: [x, y, w, h] (BOP convention)."""
        x, y, w, h = bbox
        r, g, b, a = colour
        # Rectangle outline
        draw_obj.rectangle([x, y, x + w, y + h], outline=(r, g, b, a), width=2)
        # Label text
        tx, ty = x, max(y - 14, 0)
        draw_obj.rectangle([tx, ty, tx + 70, ty + 14], fill=(r, g, b, 180))
        draw_obj.text((tx + 2, ty), label, fill=(255, 255, 255, 255))

    if f_str in scene_gt:
        for obj_idx, gt_obj in enumerate(scene_gt[f_str]):
            gt_info_list = scene_gt_info.get(f_str, [])
            if obj_idx < len(gt_info_list):
                info = gt_info_list[obj_idx]
                if show_gt:
                    draw_bbox(draw, info["bbox_obj"], COLOR_GT, f"GT obj{gt_obj['obj_id']}")
                if show_pred and f_str in predictions and obj_idx < len(predictions[f_str]):
                    # Perturb the bbox to simulate prediction error
                    bx, by, bw, bh = info["bbox_obj"]
                    rng = np.random.RandomState(frame_id * 1000 + obj_idx)
                    bx += rng.randint(-12, 12)
                    by += rng.randint(-12, 12)
                    bw += rng.randint(-8, 8)
                    bh += rng.randint(-8, 8)
                    bw = max(5, bw)
                    bh = max(5, bh)
                    pred_bbox = [max(0, bx), max(0, by), bw, bh]
                    draw_bbox(draw, pred_bbox, COLOR_PRED, f"Pred obj{gt_obj['obj_id']}")

    # Draw mask overlays
    if f_str in scene_gt:
        for obj_idx, gt_obj in enumerate(scene_gt[f_str]):
            obj_id = gt_obj["obj_id"]
            gt_mask = load_mask(frame_id, obj_idx)
            pred_mask = None
            if gt_mask is not None:
                pred_mask = generate_mock_mask_prediction(gt_mask, frame_id, obj_id)

            if show_gt and gt_mask is not None:
                mask_colour = (0, 255, 0, 60)  # semi-transparent green
                mask_rgba = np.zeros((gt_mask.shape[0], gt_mask.shape[1], 4), dtype=np.uint8)
                mask_rgba[gt_mask > 0] = mask_colour
                mask_pil = Image.fromarray(mask_rgba, "RGBA")
                overlay = Image.alpha_composite(overlay, mask_pil)

            if show_pred and pred_mask is not None:
                mask_colour = (255, 60, 60, 60)  # semi-transparent red
                mask_rgba = np.zeros((pred_mask.shape[0], pred_mask.shape[1], 4), dtype=np.uint8)
                mask_rgba[pred_mask > 0] = mask_colour
                mask_pil = Image.fromarray(mask_rgba, "RGBA")
                overlay = Image.alpha_composite(overlay, mask_pil)

            draw = ImageDraw.Draw(overlay)  # reacquire after composite

    result = Image.alpha_composite(pil_img, overlay)
    return np.array(result)


