# INTERACTIVE PLOTLY ERROR GRAPHS
# =============================================================================
def build_error_plotly(
    frame_ids: list,
    scene_gt: dict,
    predictions: dict,
    current_frame: Optional[int] = None,
) -> go.Figure:
    """Build a 3-panel Plotly subplot with hover tooltips: rotation error,
    translation error, and mask IOU.  Hovering any data point shows the
    exact frame number and error value.
    """
    # Accumulate per-object errors
    rot_errors = []   # (frame, error, obj_id)
    trans_errors = [] # (frame, error, obj_id)
    iou_errors = []   # (frame, 1-IOU, obj_id)

    for f_id in frame_ids:
        f_str = str(f_id)
        if f_str not in scene_gt or f_str not in predictions:
            continue
        for obj_idx, (gt_obj, pred_obj) in enumerate(zip(scene_gt[f_str], predictions[f_str])):
            R_gt = np.array(gt_obj["cam_R_m2c"]).reshape(3, 3)
            t_gt = np.array(gt_obj["cam_t_m2c"])
            R_pred = np.array(pred_obj["cam_R_m2c"]).reshape(3, 3)
            t_pred = np.array(pred_obj["cam_t_m2c"])
            obj_id = gt_obj["obj_id"]

            rot_errors.append((f_id, rotation_error_deg(R_gt, R_pred), obj_id))
            trans_errors.append((f_id, translation_error_mm(t_gt, t_pred), obj_id))

            gt_mask = load_mask(f_id, obj_idx)
            pred_mask = generate_mock_mask_prediction(
                gt_mask if gt_mask is not None else np.zeros((480, 640), dtype=np.uint8),
                f_id, obj_id,
            )
            if gt_mask is not None:
                iou = compute_iou(gt_mask, pred_mask)
                iou_errors.append((f_id, 1.0 - iou, obj_id))

    def _unpack(data):
        if not data:
            return [], [], []
        xs, ys, ids = zip(*data)
        return list(xs), list(ys), list(ids)

    xs_rot, ys_rot, ids_rot = _unpack(rot_errors)
    xs_trans, ys_trans, ids_trans = _unpack(trans_errors)
    xs_iou, ys_iou, ids_iou = _unpack(iou_errors)

    fig = go.Figure()

    # Panel 1: Rotation Error
    fig.add_trace(go.Scatter(
        x=xs_rot, y=ys_rot, mode="markers+lines",
        marker=dict(size=4, color="#ff9944"), line=dict(width=1, color="#ff9944"),
        name="Rot Err (deg)",
        customdata=list(zip(ids_rot)),
        hovertemplate="Frame %{x}<br>Rot Err: %{y:.2f}°<br>Obj %{customdata[0]}<extra></extra>",
        yaxis="y1",
    ))

    # Panel 2: Translation Error
    fig.add_trace(go.Scatter(
        x=xs_trans, y=ys_trans, mode="markers+lines",
        marker=dict(size=4, color="#44aaff"), line=dict(width=1, color="#44aaff"),
        name="Trans Err (mm)",
        customdata=list(zip(ids_trans)),
        hovertemplate="Frame %{x}<br>Trans Err: %{y:.2f} mm<br>Obj %{customdata[0]}<extra></extra>",
        yaxis="y2",
    ))

    # Panel 3: Mask Error (1 - IOU)
    fig.add_trace(go.Scatter(
        x=xs_iou, y=ys_iou, mode="markers+lines",
        marker=dict(size=4, color="#44ff88"), line=dict(width=1, color="#44ff88"),
        name="Mask Err (1-IOU)",
        customdata=list(zip(ids_iou)),
        hovertemplate="Frame %{x}<br>Mask Err: %{y:.4f}<br>Obj %{customdata[0]}<extra></extra>",
        yaxis="y3",
    ))

    # Current-frame vertical line annotations across all subplots
    shapes = []
    if current_frame is not None:
        shapes.append(dict(
            type="line", x0=current_frame, x1=current_frame,
            y0=0, y1=1, yref="paper",
            line=dict(color="#ffdd00", width=1.5, dash="dash"),
        ))

    # Layout: 3 rows, 1 column, shared x-axis
    fig.update_layout(
        template="plotly_dark",
        height=600,
        margin=dict(l=50, r=20, t=40, b=40),
        hovermode="x unified",
        shapes=shapes,
        grid=dict(rows=3, columns=1, pattern="independent"),
        xaxis3=dict(title="Frame ID"),
        yaxis1=dict(title="Degrees", domain=[0.68, 1.0]),
        yaxis2=dict(title="mm", domain=[0.35, 0.65]),
        yaxis3=dict(title="1 - IOU", domain=[0.0, 0.32]),
    )

    return fig


