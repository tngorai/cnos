# MAIN DASHBOARD
# =============================================================================
def main():
    print("═" * 60)
    print("  BOP Dataset Visualisation Dashboard — Part 2")
    print("  Dataset: TUD-L  |  Scene: 000001")
    print("═" * 60)

    # --- Load data ---
    scene_camera = load_json("scene_camera.json")
    scene_gt = load_json("scene_gt.json")
    scene_gt_info = load_json("scene_gt_info.json")

    frame_ids = sorted(int(k) for k in scene_camera.keys())
    min_frame, max_frame = frame_ids[0], frame_ids[-1]
    print(f"Loaded {len(frame_ids)} frames (IDs {min_frame}..{max_frame})")

    # --- Generate mock predictions ---
    print("Generating mock predictions (pose + mask)...")
    predictions = generate_mock_predictions(scene_gt)

    # Preload all mask IOU data for the global error graph
    print("Computing mask IOUs for all frames...")
    all_mask_ious = {}
    for f_id in frame_ids:
        f_str = str(f_id)
        if f_str in scene_gt:
            for obj_idx, obj in enumerate(scene_gt[f_str]):
                gt_mask = load_mask(f_id, obj_idx)
                pred_mask = generate_mock_mask_prediction(
                    gt_mask if gt_mask is not None else np.zeros((480, 640), dtype=np.uint8),
                    f_id,
                    obj["obj_id"],
                )
                if gt_mask is not None:
                    all_mask_ious[(f_id, obj["obj_id"])] = compute_iou(gt_mask, pred_mask)

    # --- Start Viser server ---
    server = viser.ViserServer()
    print("\nViser server started → http://localhost:8080\n")

    # Pre-render the Plotly error graph
    error_plotly_fig = build_error_plotly(frame_ids, scene_gt, predictions)

    # --- GUI: left panel controls ---
    with server.gui.add_folder("🎮 Controls"):
        frame_slider = server.gui.add_slider(
            "Frame",
            min=min_frame,
            max=max_frame,
            step=1,
            initial_value=min_frame,
        )
        btn_prev = server.gui.add_button("◀ Prev Frame")
        btn_next = server.gui.add_button("Next Frame ▶")
        show_gt_cb = server.gui.add_checkbox("Show Ground Truth", initial_value=True)
        show_pred_cb = server.gui.add_checkbox("Show Predictions", initial_value=True)
        stats_md = server.gui.add_markdown("*Use the slider or Prev/Next buttons to switch frames*")

    # The 2D composite image (rendered each frame change)
    with server.gui.add_folder("📷 2D View (RGB + Boxes + Masks)"):
        composite_2d = render_2d_composite(min_frame, scene_gt, scene_gt_info, predictions, True, True)
        image_gui = server.gui.add_image(
            composite_2d,
            format="png",
        )

    # Error graph panel — interactive Plotly with hover tooltips
    with server.gui.add_folder("📈 Error Graphs"):
        graph_gui = server.gui.add_plotly(
            error_plotly_fig,
            config={"displayModeBar": True, "displaylogo": False},
        )

    # 3D scene setup
    server.scene.add_icosphere(
        "/origin",
        radius=8.0,
        position=(0, 0, 0),
        color=(150, 150, 150),
    )

    # Store handles for 3D scene objects so we can clear them
    scene_handles = []

    # Preload all CAD meshes referenced in the dataset at startup
    print("Loading 3D CAD models...")
    all_obj_ids = set()
    for f_str in scene_gt:
        for obj in scene_gt[f_str]:
            all_obj_ids.add(obj["obj_id"])
    for oid in sorted(all_obj_ids):
        get_mesh(oid)  # triggers cache loading
    print(f"Cached {len(_mesh_cache)} meshes.\n")

    def draw_3d_objects(frame_id: int):
        """Draw/update 3D CAD meshes at GT (green) and Pred (red) poses."""
        nonlocal scene_handles
        for h in scene_handles:
            h.remove()
        scene_handles.clear()

        f_str = str(frame_id)
        if f_str not in scene_gt:
            return

        # Camera at origin with coordinate axes
        cam_handle = server.scene.add_frame(
            "/camera_origin",
            show_axes=True,
            axes_length=50.0,
            axes_radius=3.0,
            position=(0, 0, 0),
        )
        scene_handles.append(cam_handle)

        for obj_idx, gt_obj in enumerate(scene_gt[f_str]):
            obj_id = gt_obj["obj_id"]
            mesh = _mesh_cache.get(obj_id)

            # ---- GROUND TRUTH (solid green) ----
            if show_gt_cb.value:
                R_gt = np.array(gt_obj["cam_R_m2c"]).reshape(3, 3)
                t_gt = np.array(gt_obj["cam_t_m2c"])
                wxyz_gt = matrix_to_quat_wxyz(R_gt)

                if mesh is not None:
                    # Render the CAD model as a solid green mesh
                    h_mesh = server.scene.add_mesh_simple(
                        f"/gt_mesh_{obj_id}",
                        vertices=np.asarray(mesh.vertices, dtype=np.float32),
                        faces=np.asarray(mesh.faces, dtype=np.uint32),
                        color=(0, 180, 0),        # solid green fill
                        wireframe=False,
                        flat_shading=False,
                        position=t_gt,
                        wxyz=wxyz_gt,
                    )
                    scene_handles.append(h_mesh)
                else:
                    h_box = server.scene.add_box(
                        f"/gt_box_{obj_id}",
                        dimensions=(30, 30, 30),
                        position=t_gt,
                        wxyz=wxyz_gt,
                        color=(0, 200, 0),
                    )
                    scene_handles.append(h_box)
                # Coordinate frame for orientation reference
                h_frame = server.scene.add_frame(
                    f"/gt_frame_{obj_id}",
                    show_axes=True,
                    axes_length=30.0,
                    axes_radius=1.5,
                    position=t_gt,
                    wxyz=wxyz_gt,
                )
                scene_handles.append(h_frame)

            # ---- PREDICTION (solid red) ----
            if show_pred_cb.value and f_str in predictions and obj_idx < len(predictions[f_str]):
                pred_obj = predictions[f_str][obj_idx]
                R_pred = np.array(pred_obj["cam_R_m2c"]).reshape(3, 3)
                t_pred = np.array(pred_obj["cam_t_m2c"])
                wxyz_pred = matrix_to_quat_wxyz(R_pred)

                if mesh is not None:
                    # Same CAD model, placed at the predicted pose — solid red
                    h_mesh = server.scene.add_mesh_simple(
                        f"/pred_mesh_{obj_id}",
                        vertices=np.asarray(mesh.vertices, dtype=np.float32),
                        faces=np.asarray(mesh.faces, dtype=np.uint32),
                        color=(180, 20, 20),      # solid red fill
                        wireframe=False,
                        flat_shading=False,
                        position=t_pred,
                        wxyz=wxyz_pred,
                    )
                    scene_handles.append(h_mesh)
                else:
                    h_box = server.scene.add_box(
                        f"/pred_box_{obj_id}",
                        dimensions=(30, 30, 30),
                        position=t_pred,
                        wxyz=wxyz_pred,
                        color=(200, 40, 40),
                    )
                    scene_handles.append(h_box)
                h_frame = server.scene.add_frame(
                    f"/pred_frame_{obj_id}",
                    show_axes=True,
                    axes_length=30.0,
                    axes_radius=1.5,
                    position=t_pred,
                    wxyz=wxyz_pred,
                )
                scene_handles.append(h_frame)

    def update_all(frame_id: int):
        """Refresh 2D composite, 3D scene, error graph, and stats text."""
        # 2D composite
        comp = render_2d_composite(
            frame_id, scene_gt, scene_gt_info, predictions,
            show_gt_cb.value, show_pred_cb.value,
        )
        image_gui.image = comp

        # 3D scene
        draw_3d_objects(frame_id)

        # Error graph (redraw Plotly with current-frame marker)
        eg = build_error_plotly(frame_ids, scene_gt, predictions, frame_id)
        graph_gui.figure = eg

        # Stats markdown
        f_str = str(frame_id)
        md = f"### Frame {frame_id}\n"
        if f_str in scene_gt and f_str in predictions:
            for obj_idx, (gt_obj, pred_obj) in enumerate(zip(scene_gt[f_str], predictions[f_str])):
                R_gt = np.array(gt_obj["cam_R_m2c"]).reshape(3, 3)
                t_gt = np.array(gt_obj["cam_t_m2c"])
                R_pred = np.array(pred_obj["cam_R_m2c"]).reshape(3, 3)
                t_pred = np.array(pred_obj["cam_t_m2c"])
                obj_id = gt_obj["obj_id"]
                r_err = rotation_error_deg(R_gt, R_pred)
                t_err = translation_error_mm(t_gt, t_pred)
                iou_val = all_mask_ious.get((frame_id, obj_id), None)
                md += f"**Obj {obj_id}**\n"
                md += f"- Rot Err: `{r_err:.2f}°`\n"
                md += f"- Trans Err: `{t_err:.2f} mm`\n"
                if iou_val is not None:
                    md += f"- Mask IOU: `{iou_val:.4f}`\n"
                md += "\n"
        stats_md.content = md

